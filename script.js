const container = document.getElementById('cardContainer');

dataMahasiswa.forEach((mhs, index) => {
    const card = document.createElement('div');
    card.classList.add('card');
    card.onclick = () => openModal(index);

    card.innerHTML = `
        <img src="${mhs.foto}" alt="Foto ${mhs.nama}">
        <div class="card-info">
            <h3>${mhs.nama}</h3>
            <p>NIM: ${mhs.nim}</p>
        </div>
    `;
    
    container.appendChild(card);
});

const modal = document.getElementById("modalDetail");

function openModal(index) {
    const data = dataMahasiswa[index];
    
    document.getElementById("modalBg").style.backgroundImage = `url('${data.bg}')`;
    document.getElementById("modalProfile").src = data.foto;
    document.getElementById("modalName").innerText = data.nama;
    document.getElementById("modalNim").innerText = "NIM: " + data.nim;

    document.getElementById("modalOrigin").innerText = data.asal;
    document.getElementById("modalBirth").innerText = data.tgl_lahir;
    document.getElementById("modalFamily").innerText = data.keluarga;
    document.getElementById("modalHobby").innerText = data.hobi;
    document.getElementById("modalGoal").innerText = data.citacita;

    const igLink = document.getElementById("modalIg");
    igLink.innerText = "@" + data.ig;
    igLink.href = "https://instagram.com/" + data.ig.replace('@', '');

    document.getElementById("modalQuote").innerText = `"${data.quote}"`;

    modal.style.display = "flex";
}

function closeModal() {
    modal.style.display = "none";
}

window.onclick = function(event) {
    if (event.target == modal) {
        closeModal();
    }
}